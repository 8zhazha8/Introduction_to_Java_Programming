/**
 * Exercise 1.2 from the book Introduction to Java Programming (10th).
 * <p>
 * Write a program that displays "Welcome to Java" five times.
 * <p>
 * I choose to use a loop in this exercise, because it is the natural choice,
 * even though this concept have not yet been introduced in the text.
 * 
 * @author Henrik Samuelsson, henrik.samuelsson(at)gmail.com
 */
public class Exercise_01_02 {

	public static void main(String[] args) {
		for(int i = 0; i < 5; ++i) {
			System.out.println("Welcome to Java");
		}
	}

}
